    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        try {
            Connection c = Koneksi.MySQL();
            Statement stat = c.createStatement();
            ResultSet data = stat.executeQuery("SELECT * FROM tb_arsip WHERE " + "nama='" + txtCari.getText() + "'");
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Kode Dokumen");
            model.addColumn("Nama Dokumen");
            model.addColumn("Kategori");
            model.addColumn("Lokasi");
            model.addColumn("Deskripsi");
            model.addColumn("Tanggal");
            tblArsip.setModel(model);

            if (data.next()) {
                model.addRow(new Object[]{
                    data.getString("kode_dokumen"),
                    data.getString("nama"),
                    data.getString("kategori"),
                    data.getString("lokasi"),
                    data.getString("deskripsi"),
                    data.getString("tanggal")
                });
                tblArsip.setModel(model);
            } else {
                JOptionPane.showMessageDialog(null, "Data Tidak Ditemukan", "WARNING", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnCariActionPerformed

