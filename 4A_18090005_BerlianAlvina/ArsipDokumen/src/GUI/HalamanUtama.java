    private void loadArsip(String key) {
        try {
            model.setRowCount(0);
            Connection c = Koneksi.MySQL();
            String where = !key.isEmpty() ? "WHERE kode_dokumen LIKE '%" + key + "%'" : "";
            String query = "SELECT * FROM tb_arsip " + where;
            Statement st = c.createStatement();
            ResultSet rs = st.executeQuery(query);

            while (rs.next()) {
                int kode_dokumen = rs.getInt(1);
                String nama_dkm = rs.getString(2);
                String kategori = rs.getString(3);
                String lokasi = rs.getString(4);
                String deskripsi = rs.getString(5);
                String tanggal = rs.getString(6);
                Object[] data = {kode_dokumen, nama_dkm, kategori, lokasi, deskripsi, tanggal};
                model.addRow(data);
            }
        } catch (Exception e) {
        }
    }

