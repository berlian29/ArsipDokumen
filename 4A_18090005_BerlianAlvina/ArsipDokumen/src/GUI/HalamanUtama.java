    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int baris = tblArsip.getSelectedRow();
        if (baris != -1) {
            int pilihan = JOptionPane.showConfirmDialog(this, "Apakah anda yakin?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (pilihan == 0) {
                try {
                    int kode = Integer.parseInt(tblArsip.getValueAt(baris, 0).toString());
                    Connection c = Koneksi.MySQL();
                    String q = "DELETE FROM tb_arsip WHERE kode_dokumen=" + kode;
                    Statement st = c.createStatement();
                    st.executeUpdate(q);
                    loadArsip("");
                } catch (Exception e) {
                }
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

